import { minesweeper } from ".";

describe("Name of the group", () => {
  test("two rows", () => {
    expect(
      minesweeper(["-", "-", "-", "X", "-", "-", "-", "-", "-", "-"])
    ).toEqual(["0", "0", "1", "X", "1", "0", "0", "1", "1", "1"]);
  });
  test("five rows", () => {
    expect(
      minesweeper([
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "X",
        "X",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "-",
        "X",
      ])
    ).toEqual([
      "0",
      "0",
      "0",
      "0",
      "0",
      "2",
      "2",
      "1",
      "0",
      "0",
      "X",
      "X",
      "1",
      "0",
      "0",
      "2",
      "2",
      "1",
      "1",
      "1",
      "0",
      "0",
      "0",
      "1",
      "X",
    ]);
  });
});
